function initials(name: string): string {
  const p = name.trim().split(/\s+/).filter(Boolean)
  if (!p.length) return 'B'
  return ((p[0]![0] ?? '') + (p.length > 1 ? p[p.length - 1]![0] ?? '' : '')).toUpperCase()
}

function renderLetterhead(b: ReportBusiness): string {
  const logo = b.logoUrl
    ? `<div class="doc-logo"><img src="${escapeHtml(b.logoUrl)}" alt="" /></div>`
    : `<div class="doc-logo">${escapeHtml(initials(b.name))}</div>`
  const loc = [b.address, b.city].filter(Boolean).map((s) => escapeHtml(s as string)).join(' · ')
  const contact = [b.phone, b.email].filter(Boolean).map((s) => escapeHtml(s as string)).join(' · ')
  const addr = loc || contact ? `<div class="addr">${loc}${loc && contact ? '<br>' : ''}${contact}</div>` : ''
  const ids: Array<[string, string | null | undefined]> = [
    ['NIU', b.taxId],
    ['RCCM', b.registration],
    ['Régime', b.taxRegime],
    ['Capital', b.capital],
  ]
  const idRows = ids
    .filter(([, v]) => !!v)
    .map(([k, v]) => `<div class="row"><span class="k">${escapeHtml(k)}</span><span class="v">${escapeHtml(v as string)}</span></div>`)
    .join('')
  const idsBlock = idRows ? `<div class="doc-ids">${idRows}</div>` : ''
  return `<div class="doc-head">${logo}<div class="doc-entity">
      <div class="nm">${escapeHtml(b.name)}</div>
      ${b.type ? `<div class="form">${escapeHtml(b.type)}</div>` : ''}
      ${b.activity ? `<div class="act">${escapeHtml(b.activity)}</div>` : ''}
      ${addr}
    </div>${idsBlock}</div>`
}

function toneCls(tone?: string): string {
  return tone === 'up' ? ' up' : tone === 'down' ? ' down' : tone === 'warn' ? ' warn' : ''
}

function renderKpis(s: ReportKpiSection): string {
  const cols = Math.min(4, Math.max(1, s.items.length))
  const items = s.items
    .map(
      (k) => `<div class="doc-kpi"><div class="k">${escapeHtml(k.label)}</div>` +
        `<div class="v">${escapeHtml(k.value)}</div>` +
        `${k.hint ? `<div class="h${toneCls(k.tone)}">${escapeHtml(k.hint)}</div>` : ''}</div>`,
    )
    .join('')
  return `<div class="doc-kpis" style="grid-template-columns:repeat(${cols},1fr)">${items}</div>`
}

function cellClass(align?: string): string {
  return align === 'right' ? 'amt num' : 'lib'
}

function sectionHeader(title?: string, titleFr?: string): string {
  if (!title) return ''
  const fr = titleFr ? `<span class="frn">${escapeHtml(titleFr)}</span>` : ''
  return `<div class="doc-sec-h"><span>${escapeHtml(title)}</span>${fr}</div>`
}

function renderTable(s: ReportTableSection): string {
  const head = s.columns.map((c) => `<th class="${c.align === 'right' ? '' : 'l'}">${escapeHtml(c.label)}</th>`).join('')
  const body =
    s.rows.length === 0
      ? `<tr><td class="lib" colspan="${s.columns.length}" style="color:var(--mut)">${escapeHtml(s.empty ?? '—')}</td></tr>`
      : s.rows
          .map((row) => `<tr class="sub">${s.columns.map((c) => `<td class="${cellClass(c.align)}">${escapeHtml(row[c.key] ?? '')}</td>`).join('')}</tr>`)
          .join('')
  const total = s.total
    ? `<tr class="grand">${s.columns.map((c) => `<td class="${cellClass(c.align)}">${escapeHtml(s.total![c.key] ?? '')}</td>`).join('')}</tr>`
    : ''
  return `<div class="doc-sec">${sectionHeader(s.title, s.titleFr)}<table class="fin"><thead><tr>${head}</tr></thead><tbody>${body}${total}</tbody></table></div>`
}

function renderKeyValue(s: ReportKeyValueSection): string {
  const rows = s.rows
    .map(
      (r) => `<tr class="${r.strong ? 'grand' : r.subtotal ? 'tot' : 'sub'}"><td class="lib">${escapeHtml(r.label)}</td>` +
        `<td class="amt num${toneCls(r.tone)}">${escapeHtml(r.value)}</td></tr>`,
    )
    .join('')
  return `<div class="doc-sec">${sectionHeader(s.title, s.titleFr)}<table class="fin"><tbody>${rows}</tbody></table></div>`
}

function renderNote(s: ReportNoteSection): string {
  return `<div class="doc-note">${escapeHtml(s.text)}</div>`
}

function renderSection(s: ReportSection): string {
  switch (s.kind) {
    case 'kpis':
      return renderKpis(s)
    case 'table':
      return renderTable(s)
    case 'keyvalue':
      return renderKeyValue(s)
    case 'note':
      return renderNote(s)
  }
}

function stamp(iso: string, locale: string): string {
  try {
    return new Date(iso).toLocaleString(locale, { dateStyle: 'medium', timeStyle: 'short' })
  } catch {
    return iso
  }
}
function dateShort(iso: string, locale: string): string {
  try {
    return new Date(iso).toLocaleDateString(locale, { dateStyle: 'medium' })
  } catch {
    return iso
  }
}

/** Render a report as a self-contained statutory-style A4 HTML document (screen preview + PDF). */
export function renderReportDocumentHtml(doc: ReportDocument, opts: { labels: ReportLabels; locale?: string }): string {
  const L = opts.labels
  const locale = opts.locale ?? 'fr'
  const paper = `<div class="paper">
    ${renderLetterhead(doc.business)}
    <div class="doc-title">
      <h1>${escapeHtml(doc.title)}</h1>
      ${doc.subtitle ? `<div class="fr">${escapeHtml(doc.subtitle)}</div>` : ''}
      <div class="per"><b>${escapeHtml(doc.periodLabel)}</b></div>
    </div>
    <div class="doc-meta-row">
      <span>${escapeHtml(L.generated)} ${escapeHtml(stamp(doc.generatedAt, locale))}</span>
      ${doc.unit ? `<span class="unit">${escapeHtml(L.amountsIn)} ${escapeHtml(doc.unit)}</span>` : ''}
    </div>
    ${doc.sections.map(renderSection).join('')}
    <div class="doc-foot">
      <span class="brand"><i>B</i> ${escapeHtml(L.generatedBy)} · ${escapeHtml(dateShort(doc.generatedAt, locale))}</span>
      <span></span>
      <span>${escapeHtml(doc.business.name)}</span>
    </div>
  </div>`
  return `<!doctype html><html lang="${escapeHtml(locale)}"><head><meta charset="utf-8" /><meta name="viewport" content="width=device-width, initial-scale=1" /><title>${escapeHtml(doc.title)} · ${escapeHtml(doc.periodLabel)}</title><style>${REPORT_DOC_CSS}</style></head><body>${paper}</body></html>`
}
