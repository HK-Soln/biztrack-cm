export interface ReportLabels {
  generated: string
  generatedBy: string
  amountsIn: string
}

export function reportLabels(locale: string): ReportLabels {
  const fr = (locale || 'fr').toLowerCase().startsWith('fr')
  return fr
    ? { generated: 'Généré le', generatedBy: 'Généré par BizTrack CM', amountsIn: 'Montants en' }
    : { generated: 'Generated', generatedBy: 'Generated by BizTrack CM', amountsIn: 'Amounts in' }
}

