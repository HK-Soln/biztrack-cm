/**
 * Writes src/electron/generated/build-config.ts with the API base URL baked in at
 * build time (mirrors v1). Keeps secrets/URLs out of source while making them
 * available to the packaged Electron main.
 */
const fs = require('fs')
const path = require('path')

const apiBaseUrl =
  process.env.NEXT_PUBLIC_API_URL || process.env.DESKTOP_API_URL || 'http://localhost:3001/api/v1'

const dir = path.join(__dirname, '..', 'src', 'electron', 'generated')
fs.mkdirSync(dir, { recursive: true })
fs.writeFileSync(
  path.join(dir, 'build-config.ts'),
  `// This file is generated by scripts/generate-build-config.js\n// Do not edit manually.\n\nexport const BUILD_API_BASE_URL = ${JSON.stringify(apiBaseUrl)}\n`,
  'utf-8',
)

console.log(`✔ Wrote build-config.ts (API ${apiBaseUrl})`)
